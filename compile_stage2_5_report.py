# 格式化表格的辅助函数
def build_table_md(df):
    lines = [
        "| 标的资产 (Asset) | 非信仰型交易次数 | 信仰型交易次数 | 非信仰型空仓期比例 | 信仰型空仓期比例 | 非信仰型错过反弹收益率 | 非信仰型最终净值 | 信仰型最终净值 | 信仰溢价 (NAV Premium) |",
        "| :--- | :---: | :---: | :---: | :---: | :---: | :---: | :---: | :---: |"
    ]
    
    names_map = {
        "688_CHIP": "科创芯片 (688_CHIP)",
        "QQQ": "美股大盘 (QQQ)",
        "SOXX": "费城半导体 (SOXX)",
        "MU": "美光科技 (MU)",
        "SanDisk": "闪迪 (SanDisk)",
        "EWY": "韩国大盘 (EWY)"
    }
    
    for idx, row in df.iterrows():
        asset = row["Asset"]
        asset_name = names_map.get(asset, asset)
        non_trades = int(row["Non_Trades"])
        bel_trades = int(row["Bel_Trades"])
        non_cash = row["Non_Cash_Ratio"]
        bel_cash = row["Bel_Cash_Ratio"]
        missed = row["Non_Missed_Return"]
        non_nav = f"{row['Non_NAV']:.2f}"
        bel_nav = f"{row['Bel_NAV']:.2f}"
        premium = f"{row['Premium']:+.2f}"
        
        lines.append(
            f"| **{asset_name}** | **{non_trades} 次** | **{bel_trades} 次** | {non_cash} | {bel_cash} | **{missed}** | {non_nav} | {bel_nav} | **{premium}** |"
        )
    return "\n".join(lines)

